  const ALLOWED_PHOTO_TYPES = ['image/jpeg', 'image/png'];

  function validatePhoto(input, showAlert) {
    const file = input.files && input.files[0];
    const field = $(input).closest('.upload-field');
    let status = field.find('.file-status');
    if (!status.length) status = $('<small class="file-status"></small>').appendTo(field);

    field.removeClass('has-error');
    input.setCustomValidity('');
    if (!file) {
      status.text('Belum ada foto dipilih.');
      return !input.required;
    }

    const extensionValid = /\.(jpe?g|png)$/i.test(file.name);
    if (!ALLOWED_PHOTO_TYPES.includes(file.type) || !extensionValid) {
      field.addClass('has-error');
      status.text('Format tidak didukung. Gunakan JPG, JPEG, atau PNG.');
      input.setCustomValidity('Format foto harus JPG, JPEG, atau PNG.');
      if (showAlert) Swal.fire('Format Foto Tidak Sesuai', 'Gunakan foto berformat JPG, JPEG, atau PNG.', 'error');
      return false;
    }
    if (file.size > MAX_PHOTO_SIZE) {
      field.addClass('has-error');
      status.text('Ukuran foto melebihi batas 5 MB.');
      input.setCustomValidity('Ukuran foto maksimal 5 MB.');
      if (showAlert) Swal.fire('Foto Terlalu Besar', 'Ukuran maksimal setiap foto adalah 5 MB.', 'error');
      return false;
    }

    status.text(file.name + ' • ' + (file.size / 1024 / 1024).toFixed(2) + ' MB');
    return true;
  }

  function validatePhotos(showAlert) {
    let valid = true;
    $('#form_proses input[type="file"]').each(function() {
      if (!validatePhoto(this, showAlert && valid)) valid = false;
    });
    return valid;
  }

  $('#form_proses input[type="file"]').on('change', function() {
    validatePhoto(this, true);
  });

  function validateForm() {
    let isValid = true;
    // Get all required input fields
    $('#form_proses').find('input[required], select[required], textarea[required]').each(function() {
      if ($(this).val() === '') {
        isValid = false;
        $(this).addClass('is-invalid');
      } else {
        $(this).removeClass('is-invalid');
      }
    });
    return isValid;
  }
  $('#btn-kirim').click(function(e) {
    e.preventDefault();
    if (!validatePhotos(true)) return;
    Swal.fire({
      title: 'Apakah anda yakin?',
      text: "Data Pengajuan Toko akan di proses",
      icon: 'info',
      showCancelButton: true,
      confirmButtonColor: '#3085d6',
      cancelButtonColor: '#d33',
      cancelButtonText: 'Batal',
      confirmButtonText: 'Yakin'
    }).then((result) => {
      if (result.isConfirmed) {

        if (validateForm()) {
          document.getElementById("form_proses").submit();
        } else {
          Swal.fire({
            title: 'Belum Lengkap',
            text: ' Pastikan data di input dengan lengkap',
            icon: 'error',
            confirmButtonColor: '#3085d6',
            confirmButtonText: 'OK'
          });
        }
      }
    })
  })
</script>
