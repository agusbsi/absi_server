<script>
  (function() {
    const draftKey = 'absi_customer_toko_draft_<?= (int) $this->session->userdata('id') ?>';
    const form = document.getElementById('form_proses');
    const status = document.getElementById('draft-status');
    let saveTimer;

    function getDraftData() {
      const values = {};
      $(form).find('input:not([type="file"]):not([type="submit"]):not([type="hidden"]), select, textarea').each(function() {
        if (this.name) values[this.name] = $(this).val();
      });
      return { values: values, savedAt: Date.now() };
    }

    function saveDraft() {
      try {
        localStorage.setItem(draftKey, JSON.stringify(getDraftData()));
        status.innerHTML = '<i class="fas fa-check-circle"></i>Draft tersimpan';
      } catch (error) {
        status.innerHTML = '<i class="fas fa-exclamation-circle"></i>Draft gagal disimpan';
      }
    }

    function scheduleSave() {
      status.innerHTML = '<i class="fas fa-sync-alt"></i>Menyimpan draft...';
      clearTimeout(saveTimer);
      saveTimer = setTimeout(saveDraft, 400);
    }

    function applyRegularValues(values) {
      Object.keys(values).forEach(function(name) {
        if (['provinsi', 'kabupaten', 'kecamatan'].indexOf(name) !== -1) return;
        const field = form.elements[name];
        if (field && field.type !== 'file') $(field).val(values[name]).trigger('change.select2');
      });
    }

    function restoreRegions(values) {
      if (!values.provinsi) return;
      $('#provinsi_toko').val(values.provinsi).trigger('change.select2');
      $.get('<?= base_url('spv/Toko/add_ajax_kab') ?>/' + values.provinsi).done(function(options) {
        $('#kabupaten_toko').html(options).val(values.kabupaten || '').trigger('change.select2');
        if (!values.kabupaten) return;
        $.get('<?= base_url('spv/Toko/add_ajax_kec') ?>/' + values.kabupaten).done(function(districts) {
          $('#kecamatan_toko').html(districts).val(values.kecamatan || '').trigger('change.select2');
        });
      });
    }

    try {
      const draft = JSON.parse(localStorage.getItem(draftKey));
      if (draft && draft.values) {
        applyRegularValues(draft.values);
        restoreRegions(draft.values);
        status.innerHTML = '<i class="fas fa-history"></i>Draft berhasil dipulihkan';
      }
    } catch (error) {
      localStorage.removeItem(draftKey);
    }

    $(form).on('input change', 'input:not([type="file"]):not([type="hidden"]), select, textarea', scheduleSave);
  })();
</script>
